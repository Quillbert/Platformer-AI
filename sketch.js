var ground;
var brick;
var stack;
let pop;
let tile = [];

var offset = 15; 
var increase = 5;

function preload() {
	ground = loadImage("assets/GroundBlock.png");
	brick = loadImage("assets/Brick.png");
	stack = loadImage("assets/Stack.png");
}

function setup() {
  // put setup code here
  createCanvas(910,630);
  pop = new Population(150);
  for(let i = 0; i < 9; i++) {
  	tile[i] = [];
  	for(let j = 0; j < 100; j++) {
  		tile[i][j] = new Tile(70*j,70*i);
  		tile[i][j].identifier = layout[i][j]; 
  	}
  }
}

function draw() {
  // put drawing code here
  push();
  cameraFollow();
  background(25,195,235);
  if(pop.allDead()) {
  	pop.calculateFitness();
  	pop.naturalSelection();
  	pop.mutateDemBabies();
  } else {
  	pop.update();
  }
  for(let i = 0; i < tile.length; i++) {
  	for(let j = 0; j < tile[i].length; j++) {
  		tile[i][j].act();
  	}
  }
  pop.show();
  pop();
  fill(0);
  textSize(25);
  textAlign(LEFT, TOP);
  text("Generation: " + pop.gen, 10, 10);
}

function cameraFollow() {
	var maxX = 0;
	for(let i = 0; i < pop.players.length; i++) {
		if(!pop.players[i].dead) {
			maxX = max(maxX, pop.players[i].x);
		}
	}
	if(maxX >= 450) {
		translate(-maxX+450,0);
	}
}